public class ComparisonOperators {
    public static void main(String[] args) {
        // equal
        System.out.println(5 == 5);
        // distinc
        System.out.println( 5 != 5);
        System.out.println( 5 != 4);

        // bigger than
        System.out.println( 5 > 5);
        System.out.println( 5 > 4);

        // smaller than
        System.out.println( 5 < 5);
        System.out.println( 4 < 5);

        // bigger or equal than
        System.out.println( 5 >= 5);
        System.out.println( 5 >= 4);

        // smaller or equal than
        System.out.println( 5 <= 5);
        System.out.println( 4 <= 5);
    }
}
