public class AssignmentOperators {

    public static void main(String[] args) {
        int resultado = 0;

        resultado += 2;
        System.out.println("resultado = " + resultado);

        resultado *= 2;
        System.out.println("resultado = " + resultado);

        resultado /= 2;
        System.out.println("resultado = " + resultado);

        resultado -= 2;
        System.out.println("resultado = " + resultado);

    }
}
